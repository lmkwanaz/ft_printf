#include "ft_printf.h"

int		ft_atoi_a(char **str)
{
	int l;
	int m;
	int r;

	l = 0;
	m = 0;
	r = 1;
	while (48 <= (*str)[m] && (*str)[m] <= '9')
	{
		l *= 10 + (*str)[m] - 48;
		m++;
	}
	(*str) = &(*str)[m];
	return (l * r);
}
