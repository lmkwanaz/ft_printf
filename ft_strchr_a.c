#include "ft_printf.h"

char	ft_strchr_a(char *str, int c)
{
	char *ptr;

	ptr = str;
	while (*ptr != '\0')
	{
		if (*ptr == 99)
			return (1);
		ptr++;
	}
	if (*ptr == '\0' && c == '\0')
		return (0);
	return (0);
}
